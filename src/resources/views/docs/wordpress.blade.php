    <h2>Step 1: Download and Prepare the Plugin</h2>
    <p>Start by visiting the Sendeach website and downloading the WordPress plugin to your computer.</p>
    <a href="https://sendeach.com/assets/wordpress/sendeach.zip" class="btn btn-primary" download>Download Sendeach Plugin</a>
    <p>Next, log into your WordPress website's admin panel.</p>
    <p>Navigate to the 'Plugins' section and click 'Add New'.</p>
    <p>Click the 'Upload Plugin' button, then choose the plugin file you downloaded.</p>
    <p>Click 'Install Now' and wait for the plugin to be installed.</p>
    <p>After installation, click 'Activate' to enable the Sendeach plugin on your website.</p>

    <h2>Step 2: Install the Plugin</h2>
    <p>1. Log in to your WordPress admin dashboard.</p>
    <p>2. Navigate to the "Plugins" section in the left-hand menu and click on "Add New."</p>
    <p>3. Select the "Upload Plugin" option.</p>
    <p>4. Click the "Choose File" button and locate the Sendeach plugin file you downloaded earlier.</p>
    <p>5. Once selected, click the "Install Now" button to begin the installation.</p>
    <p>6. After installation, click "Activate" to enable the Sendeach plugin on your website.</p>

    <h2>Step 3: Explore Plugin Options</h2>
    <p>1. With the plugin activated, navigate to the "Plugins" page and find the Sendeach plugin.</p>
    <p>2. Click on the "Sendeach" plugin to access its settings.</p>
    <p>3. Inside the plugin, you'll discover various self-explanatory options.</p>
    <p>4. These options include login and registration forms, as well as exporting website registered users.</p>
    <p>5. Take some time to explore these settings and understand how they can enhance your site.</p>

    <h2>Step 4: Configure API Key</h2>
    <p>1. Inside the plugin settings, locate the API key configuration section.</p>
    <p>2. Uncheck the "Admin Key" checkbox to enable advanced features.</p>
    <p>3. Open a new tab and log in to your Sendeach dashboard.</p>
    <p>4. Navigate to your user profile and click on "API Token."</p>
